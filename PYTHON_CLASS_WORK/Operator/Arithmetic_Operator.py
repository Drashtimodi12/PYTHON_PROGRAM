#Arithmatic Operatos : +,-,*,/,%,**,//

print(10+10)    # Addition: 10 + 10 = 20
print("a"+"b")  # String concatenation: 'a' + 'b' = 'ab'
# print("a"+10)   # This will cause a TypeError because you can't add string and integer

a = 10+10.00    # Mixing int and float results in float
print(type(a))  # Output: <class 'float'>
print(a)        # Output: 20.0

print(10%3)     # Modulus: 10 % 3 = 1 (remainder)

print(10**2)    # Exponentiation: 10^2 = 100

print(16//3)    # Floor Division: 16 // 3 = 5

x = 10
y = 3

print(x + y)  # Addition: 10 + 3 = 13
print(x - y)  # Subtraction: 10 - 3 = 7
print(x * y)  # Multiplication: 10 * 3 = 30
print(x / y)  # Division: 10 / 3 = 3.3333
print(x // y)  # Floor Division: 10 // 3 = 3
print(x % y)  # Modulus: 10 % 3 = 1 (remainder)
print(x ** y)  # Exponentiation: 10^3 = 1000





