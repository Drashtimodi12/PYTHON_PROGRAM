# 3. Conditional Statements     Conditional Statement (if age >= 18:) checks if a person is an adult or minor.
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# 4. Loops: Printing numbers from 1 to 5 using a for loop       Loop (for i in range(1, 6)) prints numbers from 1 to 5.
print("Counting: ")
for i in range(1, 6):  
    print(i)