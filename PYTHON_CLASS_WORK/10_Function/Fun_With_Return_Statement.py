# Function with return Statement:- In Python, the return statement is used in a function to send back a value as an output. 
# This allows us to store the result in a variable or use it in further calculations.
# A function that calculates the cube of a number and returns the result.

# Function that takes a number as input
def cube(a) :
    return a * a * a        # Returns the cube of the given number

# Calling the function and storing the result
result = cube(3)
print(result)

# Output: 27