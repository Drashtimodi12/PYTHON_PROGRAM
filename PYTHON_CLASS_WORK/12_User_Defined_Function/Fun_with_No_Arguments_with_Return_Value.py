# 3. Function with No Arguments but with Return Value
# No input is taken, but it returns a value.

def get_name():
    return "Drashti" 

name = get_name() # Call the function and store the returned value in 'name'
# The function get_name() does not take any arguments, but it returns a string value "
print(name)

# Output: Drashti
# This function is useful when you want to encapsulate logic that generates or 
# retrieves a value without needing any input parameters.