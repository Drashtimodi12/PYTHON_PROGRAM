# 18.	Write a Python program to access the value of the first index in a tuple.

# Define a tuple
mytuple = ("apple", "banana", "mango", "orange", "kiwi")

# Access elements using indexing
print("First Fruit: ", mytuple[0])           # First Fruit:  apple
print("Second Fruit: ", mytuple[1])          # Second Fruit:  banana
print("Third Fruit: ", mytuple[2])           # Third Fruit:  mango
print("Last Friut: ", mytuple[-1])           # Last Friut:  kiwi
print("Second Last Fruit: ", mytuple[-2])    # Second Last Fruit:  orange
print("All Fruits: ", mytuple[0:5])          # All Fruits:  ('apple', 'banana', 'mango', 'orange', 'kiwi')   