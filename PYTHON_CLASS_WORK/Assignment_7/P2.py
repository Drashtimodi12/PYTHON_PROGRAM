# 2.	Write a Python program to access elements at different index positions.

# Define the list
mylist = ["apple", "banana", "orange", "Mango", "kiwi"]

# Access elements using indexing
print("First Fruit: ", mylist[0])           # First Fruit:  apple
print("Second Fruit: ", mylist[1])          # Second Fruit:  banana
print("Third Fruit: ", mylist[2])           # Third Fruit:  orange
print("Last Friut: ", mylist[-1])           # Last Friut:  kiwi
print("Second Last Fruit: ", mylist[-2])    # Second Last Fruit:  Mango
print("All Fruits: ", mylist[0:5])          # All Fruits:  ['apple', 'banana', 'orange', 'Mango', 'kiwi']   