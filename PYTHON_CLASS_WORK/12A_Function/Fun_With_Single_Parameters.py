# Function with a Single Parameter:- A function with a single parameter takes one input and performs an operation using that input.
# A function that calculates the square of a number.

# Define a function named 'square' that takes one parameter 'a'
def square(a):
    # Print the square of the given number
    print(a * a)

# Call the function with the argument 10
square(10)  

# Output: 100