# Assignment Operators: =, +=, -=, *=, /=, %=, **=, //=

a = 10  # Assign value 10 to a
print(a)  # Output: 10

a = a + 5
a += 5  # Equivalent to a = a + 5
print(a)  # Output: 15

a -= 3  # Equivalent to a = a - 3
print(a)  # Output: 12

a *= 2  # Equivalent to a = a * 2
print(a)  # Output: 24

a /= 4  # Equivalent to a = a / 4
print(a)  # Output: 6.0

a %= 5  # Equivalent to a = a % 5 (remainder after division by 5)
print(a)  # Output: 1.0

a **= 3  # Equivalent to a = a ** 3 (power operation)
print(a)  # Output: 1.0

a //= 2  # Equivalent to a = a // 2 (floor division)
print(a)  # Output: 0.0
