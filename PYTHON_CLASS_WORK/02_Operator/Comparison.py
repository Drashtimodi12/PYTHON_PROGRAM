#  Comparison Operator: ==, =!, >, <, >=, <=

a = 10
b = 5

# Equal to (==)
print(a == 10)  # True
print(b == 10)  # False

# Not equal to (!=)
print(a != b)  # True
print(a != 10)  # False

# Greater than (>)
print(a > b)  # True
print(b > a)  # False

# Less than (<)
print(a < b)  # False
print(b < a)  # True

# Greater than or equal to (>=)
print(a >= 10)  # True
print(b >= 10)  # False

# Less than or equal to (<=)
print(a <= 10)  # True
print(b <= 10)  # True