# 4.	Write a Python program to read a string, an integer, and a float from the keyboard and display them. 

user_string = input("Enter String: ")       # Read a string from the user
user_int = int(input("Enter Integer Number: "))     # Read a integer from the user
user_float = float(input("Enter Float Number: "))   # Read a float from the user

# Display the inputs
print(f"\nYou entered the string: {user_string}")
print(f"You entered the integer: {user_int}")
print(f"You entered the float: {user_float}")

# Output: 
# Enter String: abcd ef
# Enter Integer Number: 12
# Enter Float Number: 12.77

# You entered the string: abcd ef
# You entered the integer: 12
# You entered the float: 12.77