# 1.	Write a Python program to print a formatted string using print() and f-string. 

# Define variables
Name = "Drashti"
Age = 25
City = "Surat"

# Print a formatted string using an f-string
print(f"""My name is {Name}. 
i am {Age} old.
i live at {City}.""")

# Output: 
# My name is Drashti. 
# i am 25 old.        
# i live at Surat. 