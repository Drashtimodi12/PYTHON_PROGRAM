# 3.	Write a Python program to read a name and age from the user and print a formatted output. 

Name = input("Enter Your name: ")       # Read a name from the user
Age = int(input("Enter your Age: "))    # Read age from the user


# Print a formatted output using an f-string
print(f"\nYour Name is {Name}.")
print(f"Yur Age is {Age}.")

# Output: 
# Enter Your name: drashti
# Enter your Age: 25

# Your Name is drashti.
# Yur Age is 25.